class Rect:

    def __init__(self, x, y, width, height):
        self.x = x
        self.y = y
        self.width = width
        self.height = height


class Size:

    def __init__(self, width, height):
        self.width = width
        self.height = height


class Point:

    def __init__(self, x, y):
        self.x = x
        self.y = y